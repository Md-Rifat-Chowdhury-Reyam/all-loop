package ForLoop;

public class Infinite1 {

    public static void main(String[] args) {
        for(int i = 1; i >= 1; i++)
        {
            if(i % 2 == 0)
            {
                System.out.print("1");
            }
            else
            {
                System.out.print("0");
            }
        }
    }
}
